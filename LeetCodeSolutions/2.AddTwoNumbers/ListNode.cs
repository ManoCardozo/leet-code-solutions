using System;
using System.Collections.Generic;
using System.Text;

namespace LeetCodeSolutions._2.AddTwoNumbers
{
    //Definition for singly-linked list.
    public class ListNode
    {
        public int val;
        public ListNode next;
        public ListNode(int val = 0, ListNode next = null)
        {
            this.val = val;
            this.next = next;
        }
    }
}
